#include <stdio.h>
/**
 * main - use putchar function
 *
 * Description: using main function
 * this program prints lowercase alphabet
 * Return: 0
 */
int main(void)
{
	char ch;

	for (ch = 'a'; ch <= 'z'; ch++)
	{
		if (ch != 'e' && ch != 'q')
		{
			putchar(ch);
		}
	}
	putchar('\n');
	return (0);
}
